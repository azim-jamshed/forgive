<p><a href="https://peoplesharingjesus.com/missions/">Crestview Baptist Church</a> responded to an invitation from Dr. Paul Negrut of Emanuel University (<a href="http://www.emanuel.ro/">http://www.emanuel.ro/</a>) by sending a team of 38 members to Oradea, Romania. The purpose of the trip was three-fold:
<ol> 
<li>Provide a music and evangelism program to the university and regional Baptist churches of Romania and Moldova.</li>
<li>Have fellowship with the young ministers, teachers and businessmen and women in training at the university.</li>
<li>Assess and begin planning for future mission and ministry opportunities.</li>
</ol>
While in Romania, the team provided music and preaching at several churches. They took part in street evangelism, visited orphanages and took part in many social service activities. Over the course of the trip there were several professions of faith made during ministry activities. Team members met with students, provided teaching and lectures, and met with local business leaders. Over 200 Romanian bibles were provided by the group. 
</p> 

