<h3><b><i>Cheryl Summers's Fabulous Seafood Casserole</i></b></h3>
Ingredients:
</td>

</tr>

<tr>
<td><h6>
2 medium eggplants<br>
2 cups lump crab meat<br>	
1 lb. shrimp, peeled & de-veined<br>	
1 pt. oysters, drained (optional)<br>	
1-1/2 cups seasoned bread crumbs<br>	
1 bunch spring onions, finely sliced<br>
1 bell pepper, chopped<br> 
2 ribs of celery, chopped<br>
1 stick butter or margarine, plus slices<br>
dash or two of hot sauce (optional)
</h6></td>
<td><h6>
<p>Peel and dice eggplant; boil in lightly salted water until tender. Drain and set aside. Sauteé vegetables in butter or margarine until slightly soft (cook celery first). Toss in eggplant cubes, stirring until coated with butter. Salt and pepper to taste. Add hot sauce if desired. Preheat oven to 350 degrees;.</p>
<p>In deep casserole dish coated with non-stick cooking spray, place layer of Sauteéd eggplant mixture. Alternate layers of crab meat, sprinkling of bread crumbs, eggplant, shrimp, bread crumbs, oysters, and eggplant. Top with layer of bread crumbs; dot with pats of butter or margarine.</p>
<p>Bake for 30 to 40 minutes (until bubbly around edges). Serve with salad and French bread. Enjoy!</p>
</td>
