<b>Mrs. Martin's Best-Ever Gumbo</b><br>
Ingredients:
</td></tr>
<tr><td>
<h6>
<ul>
<li>1 lb. shrimp, peeled & de-veined
<li>1 lb. smoked sausage, sliced
<li>16 oz. pkg. frozen cut okra, thawed
<li>1 lg. onion, chopped
<li>3 tbsp. cooking oil, divided
<li>16 oz. can stewed tomatoes, chopped
<li>2 tbsp. all-purpose flour
<li>2 qt. hot water
<li>3 bay leaves
</ul></td>
<td><h6><p>Sauteé flour in 2 tbsp. oil until toasty brown. Slowly stir in water, stirring constantly to keep mixture smooth; add bay leaves. Simmer, stirring occasionally. In separate pan, brown sausage in 1 tbsp. oil. Add okra, onions, and tomatoes; stir until okra is warmed through. Add sausage-vegetable mixture to broth, stirring thoroughly. Cover and simmer for 1 1/2 hrs, stirring occasionally. Salt and pepper to taste. Add shrimp and cook for 30 more minutes. Serve over rice.</p>
