<p><b style='mso-bidi-font-weight:normal'><span
style='color:purple'>Make Blanket:</span></b></p>

<p  style='margin-left:.25in'><b style='mso-bidi-font-weight:
normal'><span style='color:purple'></span></b></p>

<p><span style='color:purple'>Cast on <a href="/modifications">92</a> <span class=GramE>sts</span>.
Knit 8 rows.</span></p>



<p><b style='mso-bidi-font-weight:normal'><span
style='color:purple'>Begin eyelet section</span></b><span style='color:purple'>:</span></p>



<p><span style='color:purple'>Row 1: K4, *YO, K2tog, repeat
from * across, to last 4 sts. YO, K4. (93 stitches on needle)</span></p>



<p><span style='color:purple'>Row 2: K4, P across to last 4 <span
class=GramE>sts</span>, k4.</span></p>



<p><span style='color:purple'>Row 3: K4, YO, ssk, K across to
last 7 sts. <span class=GramE>K3tog, YO, K4. (92 stitches on needle)</span></span></p>



<p><b style='mso-bidi-font-weight:normal'><span
style='color:purple'>Begin Basket-Weave Pattern (12 rows)</span></b></p>


<p><span style='color:purple'>Row 1 (Wrong side): K4, P2, *K4,
P4, repeat from * across to last 6 sts, P2, K4.</span></p>

<p><span style='color:purple'>Row 2: (Right side): K4, YO, ssk, *K4, P4, repeat from * across to last 6 sts,
K2tog, YO, K4.</span></p>


<p><span style='color:purple'>Rows 3 &amp; 5: Repeat Row 1.</span></p>



<p><span style='color:purple'>Rows 4 &amp; 6: Repeat Row 2.</span></p>



<p><span style='color:purple'>Row 7: K4, P2, *P4, K4, repeat from
* across to last 6 sts, P2<span class=GramE>,<span style='mso-spacerun:yes'> 
</span>K4</span>.</span></p>



<p><span style='color:purple'>Row 8: K4, YO, SSK, *P4, K4,
repeat from * across to last 6 sts, K2tog, YO, K4.</span></p>



<p><span style='color:purple'>Rows 9 &amp; 11: Repeat row 7.</span></p>



<p><span style='color:purple'>Rows 10 &amp; 12: Repeat row 8.</span></p>



<p><span style='color:purple'>Repeat rows 1-12 until blanket
measures approx. 35 inches, ending with row 12 (right side).</span></p>



<p><b style='mso-bidi-font-weight:normal'><span
style='color:purple'>Ending eyelet section</span></b><span style='color:purple'>:</span></p>



<p><span style='color:purple'>Row 1: K4, P across to last 4 <span
class=GramE>sts</span>, k4.</span></p>



<p><span style='color:purple'>Row 2: K4, YO, ssk, *YO, K2tog,
repeat from * across to last 6 sts. <span class=GramE>K2tog, YO, K4.</span></span></p>



<p><span style='color:purple'>Row 3: K4, P across to last 4 <span
class=GramE>sts</span>, K4.<span style='mso-spacerun:yes'>  </span>Knit next 8
rows, bind off. Tuck in any loose ends, block if desired.</span></p>
